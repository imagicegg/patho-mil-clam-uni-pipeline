import os
import torch
import torch.nn as nn
import torch.optim as optim
import pandas as pd

from torch.utils.data import Dataset, DataLoader

try:
    from my_scripts.attention_mil import AttentionMIL
except ModuleNotFoundError:
    from attention_mil import AttentionMIL


# ============================================================
# 1. 自定义病理 WSI 特征数据集
# ============================================================
# 该数据集用于读取已经预提取好的病理切片特征(.pt文件)。
#
# 当前流程属于典型的 MIL (Multiple Instance Learning，多实例学习)：
#
# 一张 Whole Slide Image (WSI) 会被切分成大量 Patch，
# 然后通过特征提取网络（例如 ResNet / UNI / CONCH / Phikon）
# 提前提取为 embedding 特征。
#
# 因此：
#   每个 .pt 文件 = 一张病理切片的全部 Patch 特征
#
# 数据结构示例：
#   slide_001.pt -> Tensor [N, D]
#
# 其中：
#   N = patch数量（每张切片不同）
#   D = 特征维度（例如1024）
#
# Attention MIL 模型会对 N 个 patch 特征进行注意力聚合，
# 最终输出该切片的分类结果。
# ============================================================
class WsiFeatureDataset(Dataset):

    def __init__(self, csv_file, feature_dir):
        """
        参数:
            csv_file:
                数据标签文件路径。
                格式示例:
                    slide_id,label
                    case001,0
                    case002,1

            feature_dir:
                保存 .pt 特征文件的目录。
        """

        # 读取标签 CSV
        self.data_frame = pd.read_csv(csv_file)

        # 特征目录
        self.feature_dir = feature_dir

    def __len__(self):
        """
        返回数据集总样本数。
        """
        return len(self.data_frame)

    def __getitem__(self, idx):
        """
        根据索引读取单个 WSI 样本。

        返回:
            features:
                当前切片的 patch 特征张量
                Shape:
                    [N, D]

            label:
                当前切片对应标签
        """

        # -----------------------------
        # 读取当前切片 ID
        # -----------------------------
        slide_id = self.data_frame.iloc[idx, 0]

        # 读取分类标签
        label = int(self.data_frame.iloc[idx, 1])

        # -----------------------------
        # 构建 .pt 特征文件路径
        # -----------------------------
        pt_path = os.path.join(
            self.feature_dir,
            f"{slide_id}.pt"
        )

        # -----------------------------
        # 加载预提取特征
        # -----------------------------
        # map_location='cpu':
        #   强制加载到 CPU，避免显存占用
        #
        # weights_only=True:
        #   PyTorch 新版推荐写法
        #   更安全、更快
        # -----------------------------
        features = torch.load(
            pt_path,
            map_location='cpu',
            weights_only=True
        )

        # 返回:
        #   patch特征
        #   标签tensor
        return features, torch.tensor(label)


# ============================================================
# 2. 主训练流程
# ============================================================
if __name__ == "__main__":

    # ========================================================
    # 设备初始化
    # ========================================================
    # 当前使用 CPU 训练。
    #
    # 后续如果切换 GPU:
    #   device = torch.device("cuda")
    #
    # Intel Arc:
    #   可尝试使用:
    #       torch.device("xpu")
    #
    # NVIDIA:
    #   推荐 CUDA:
    #       torch.device("cuda")
    # ========================================================
    device = torch.device("cpu")

    print(f"启动极速训练模式，使用设备: {device}")

    # ========================================================
    # 构建数据集
    # ========================================================
    # 当前仅使用16张切片进行快速验证，
    # 目标是确认:
    #
    #   数据读取
    #   MIL聚合
    #   前向传播
    #   反向传播
    #   权重更新
    #
    # 是否全部正常。
    # ========================================================
    dataset = WsiFeatureDataset(
        csv_file="data/dataset.csv",
        feature_dir="data/results/features"
    )

    # ========================================================
    # DataLoader
    # ========================================================
    # batch_size 必须设置为 1
    #
    # 原因:
    #   不同WSI包含的 patch 数量不同。
    #
    # 示例:
    #   slide_A -> [5421, 1024]
    #   slide_B -> [7812, 1024]
    #
    # PyTorch 默认无法直接 stack。
    #
    # 因此 MIL 训练通常:
    #   batch_size = 1
    #
    # 或者:
    #   自定义 collate_fn
    # ========================================================
    dataloader = DataLoader(
        dataset,
        batch_size=1,
        shuffle=True
    )

    # ========================================================
    # 初始化 Attention MIL 模型
    # ========================================================
    # AttentionMIL 的核心思想:
    #
    #   1. 输入:
    #       所有 patch 特征
    #
    #   2. Attention模块:
    #       学习哪些 patch 更重要
    #
    #   3. Attention加权聚合:
    #       得到整张WSI的全局表示
    #
    #   4. 分类头:
    #       输出最终类别
    #
    # Attention机制能够自动定位:
    #   肿瘤区域
    #   高风险区域
    #   关键病理结构
    # ========================================================
    model = AttentionMIL().to(device)

    # ========================================================
    # 损失函数
    # ========================================================
    # CrossEntropyLoss:
    #
    # 适用于:
    #   多分类
    #   二分类(logits输出)
    #
    # 注意:
    #   模型输出不要提前 softmax。
    # ========================================================
    criterion = nn.CrossEntropyLoss()

    # ========================================================
    # 优化器
    # ========================================================
    # Adam:
    #   深度学习中最常用优化器之一
    #
    # lr=1e-4:
    #   MIL任务中较常见的稳定学习率
    # ========================================================
    optimizer = optim.Adam(
        model.parameters(),
        lr=1e-4
    )

    # ========================================================
    # 训练轮数
    # ========================================================
    epochs = 10

    print("\n开始训练网络...")

    # ========================================================
    # Epoch 训练循环
    # ========================================================
    for epoch in range(epochs):

        # 切换到训练模式
        model.train()

        total_loss = 0.0
        correct = 0

        # ====================================================
        # 遍历每张 WSI
        # ====================================================
        for features, label in dataloader:

            # ------------------------------------------------
            # 数据移动到训练设备
            # ------------------------------------------------
            features = features.to(device)
            label = label.to(device)

            # ------------------------------------------------
            # 梯度清零
            # ------------------------------------------------
            # PyTorch 默认梯度累积，
            # 因此每次迭代前必须清零。
            # ------------------------------------------------
            optimizer.zero_grad()

            # ------------------------------------------------
            # 前向传播
            # ------------------------------------------------
            # 输出:
            #
            # output:
            #   分类 logits
            #
            # attention_weights:
            #   每个 patch 的注意力权重
            #
            # attention_weights 可用于:
            #   病灶可视化
            #   热力图生成
            #   可解释性分析
            # ------------------------------------------------
            output, attention_weights = model(features)

            # ------------------------------------------------
            # 计算损失
            # ------------------------------------------------
            loss = criterion(output, label)

            # ------------------------------------------------
            # 反向传播
            # ------------------------------------------------
            # 自动计算所有参数梯度
            # ------------------------------------------------
            loss.backward()

            # ------------------------------------------------
            # 更新模型参数
            # ------------------------------------------------
            optimizer.step()

            # ------------------------------------------------
            # 统计 Loss
            # ------------------------------------------------
            total_loss += loss.item()

            # ------------------------------------------------
            # 计算预测类别
            # ------------------------------------------------
            pred = output.argmax(dim=1)

            # ------------------------------------------------
            # 统计正确样本数
            # ------------------------------------------------
            correct += int(pred == label)

        # ====================================================
        # Epoch 统计
        # ====================================================
        acc = correct / len(dataset)

        print(
            f"Epoch [{epoch+1}/{epochs}] "
            f"| Loss: {total_loss:.4f} "
            f"| 准确率: {acc*100:.2f}%"
        )

    # ========================================================
    # 训练结束
    # ========================================================
    print("恭喜！整个病理 AI 流水线已彻底跑通！")

    # ========================================================
    # 保存模型权重
    # ========================================================
    # state_dict:
    #   仅保存模型参数
    #
    # 优点:
    #   文件更小
    #   更推荐
    #   更通用
    # ========================================================
    torch.save(
        model.state_dict(),
        "data/best_clam_model.pth"
    )

    print("模型权重已保存至 data/best_clam_model.pth")